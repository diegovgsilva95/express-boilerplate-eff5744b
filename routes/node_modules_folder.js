"use strict";

const
    path = require("path"),
    { Router, "static": xp_static } = require("express"),
    router = Router();

const NODE_MODULES_PATH = path.resolve(process.mainModule.path, "node_modules");

router.use(
    xp_static(NODE_MODULES_PATH, {
        cacheControl: false,
        etag: false,
    })
);
module.exports = exports = router;